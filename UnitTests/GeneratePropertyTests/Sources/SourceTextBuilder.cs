    /// <summary>
    /// 代码段开始(花括号)
    /// </summary>
    public void BuildBeigin()
    {
        BuildBeigin(_builder, ref _indent);
    }
    /// <summary>
    /// 代码段结束(花括号)
    /// </summary>
    public void BuildEnd()
    {
        BuildEnd(_builder, ref _indent);
    }
    /// <summary>
    /// 缩进
    /// </summary>
    /// <param name="builder"></param>
    /// <returns></returns>
    public StringBuilder GenerateIndent()
    {
        return BuildIndent(_builder, _indent);
    }
    /// <summary>
    /// 添加修饰符
    /// </summary>
    /// <param name="modifiers"></param>
    /// <returns></returns>
    public StringBuilder BuildModifiers(params IEnumerable<string> modifiers)
    {
        return BuildModifiers(_builder, modifiers);
    }
    #endregion
    #region Build
    /// <summary>
    /// 代码段开始(花括号)
    /// </summary>
    /// <param name="builder"></param>
    /// <param name="indent">缩进数量</param>
    public static void BuildBeigin(StringBuilder builder, ref int indent)
    {
        BuildIndent(builder, indent)
            .AppendLine("{");
        indent += 4;
    }
    /// <summary>
    /// 代码段结束(花括号)
    /// </summary>
    /// <param name="builder"></param>
    /// <param name="indent">缩进数量</param>
    public static void BuildEnd(StringBuilder builder, ref int indent)
    {
        indent -= 4;
        BuildIndent(builder, indent)
            .AppendLine("}");        
    }
    /// <summary>
    /// 缩进
    /// </summary>
    /// <param name="builder"></param>
    /// <param name="indent"></param>
    /// <returns></returns>
    public static StringBuilder BuildIndent(StringBuilder builder, int indent)
    {
        if (indent > 0)
            builder.Append(' ', indent);
        return builder;
    }
    /// <summary>
    /// 添加修饰符
    /// </summary>
    /// <param name="builder"></param>
    /// <param name="modifiers"></param>
    /// <returns></returns>
    public static StringBuilder BuildModifiers(StringBuilder builder, params IEnumerable<string> modifiers)
    {
        foreach (string modifier in modifiers)
        {
            builder.Append(modifier)
                .Append(' ');
        }
        return builder;
    }
    #endregion
    #region Scope
    /// <summary>
    /// 代码块
    /// </summary>
    /// <returns></returns>
    public BlockScope Block()
        => new(this);
    /// <summary>
    /// 命名空间
    /// </summary>
    /// <param name="ns"></param>
    /// <returns></returns>
    public BlockScope NameSpace(INamespaceSymbol ns)
        => NameSpace(ns.ToDisplayString());
    /// <summary>
    /// 命名空间
    /// </summary>
    /// <param name="ns"></param>
    /// <returns></returns>
    public BlockScope NameSpace(string ns)
    {
        AppendLine($"namespace {ns}");
        return new(this);
    }
    /// <summary>
    /// 锁代码块
    /// </summary>
    /// <param name="name"></param>
    /// <returns></returns>
    public BlockScope Lock(string name)
    {
        AppendLine($"lock({name})");
        return new(this);
    }
    /// <summary>
    /// 
    /// </summary>
    /// <param name="name"></param>
    /// <param name="statement"></param>
    /// <returns></returns>
    public BlockScope Using(string name, string statement)
    {
        AppendLine($"using(var {name} = {statement})");
        return new(this);
    }
    #endregion
    /// <summary>
    /// 拼接全部字符
    /// </summary>
    /// <returns></returns>
    public override string ToString()
        => _builder.ToString();
}
